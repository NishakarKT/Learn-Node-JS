const os = require('os');

// architecture
console.log(os.arch());

// Free RAM
console.log(os.freemem());

// Total RAM
console.log(os.totalmem());

// Host name
console.log(os.hostname());

// Platform
console.log(os.platform());

// Temporary Directory
console.log(os.tmpdir());

// Operating system
console.log(os.type());